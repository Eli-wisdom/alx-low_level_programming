#include <stdio.h>
/**
 * main - prints all the numbers of base 16 in lowercase,
 * then a new line
 * Return: Always 0 (Success)
 */
int main(void)
{
	int n;
	char cha;

	for (n = 48; n < 58; n++)
	{
		putchar(n);
	}
	for (cha = 'a'; cha <= 'f'; cha++)
	{
		putchar(ch);
	}
	putchar('\n');
	return (0);
}

